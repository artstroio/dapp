const contractAddress = '0x4ab23b8f65F4CbADCB796fE4913b1d43399B13e8'

let imgBuffer,ipfs,imageUrl,description,supply,value,royalty,optionalUrl,tokenURI,contract;

window.onload = async () => {
    loader.hidden = true
    mintLoader.hidden = true
     ipfs = IpfsApi({
         host: 'ipfs.infura.io',
         port: 5001,
         protocol: 'https'
     })
    if(window.ethereum) {
        web3 = new Web3(window.ethereum)
        try{    
            contract = new web3.eth.Contract(contractAbi,contractAddress);
            accounts = await ethereum.enable();
        }catch(e){
            console.log(e);
        }
    }
}

inputELm.addEventListener('change' ,(e) => {
    loader.hidden = false
    const file = e.target.files[0];
    const reader = new window.FileReader()
    reader.readAsArrayBuffer(file);
    reader.onloadend = () => {
        imgBuffer = ipfs.Buffer(reader.result);
    }
    setTimeout(() => {
        addData()
    }, 500);

})

let hash;

async function addData() {
    await  ipfs.files.add(imgBuffer,(err,result) => {
        if(err) {
            console.log(err);
        }
        hash = result[0]
        console.log(hash)
        loader.hidden = true
        imageUrl = (`https://gateway.pinata.cloud/ipfs/${result[0].hash}`)
    })
}

sendJSONToIPFS = async () => {
    mintBtnELm.value = 'Sending metadata to IPFS'
    mintLoader.hidden = false;
    getValues()
    const doc = JSON.stringify({
        description: description,
        supply: supply,
        value: value,
        royalty: royalty,
        optionalUrl: optionalUrl,
        img: imageUrl
    });
    
    ipfs.add(ipfs.Buffer(doc)).then((cid) => {

        console.log("IPFS cid:", cid);
        mintLoader.hidden = true;
        tokenURI = `https://gateway.pinata.cloud/ipfs/${cid[0].hash}`
        console.log(`https://gateway.pinata.cloud/ipfs/${cid[0].hash}`)  
        mintBtnELm.value = 'Sending Transaction to BSC'
        mintLoader.hidden = false;
        mintToken()
    })
  }

function getValues() {
    description = descriptionELm.value;
    supply = supplyELm.value;
    value = valueElm.value;
    royalty = royaltyELm.value;
    optionalUrl = optionalURLELm.value;
}

function mintToken() {
    contract.methods.mint(accounts[0],supply,tokenURI,web3.utils.toWei(value)).
    send({from: accounts[0],value : '100000000000000000'}).then(() =>{
        mintBtnELm.value = 'Transaction Confirmed (token minted)'
        mintLoader.hidden = true;
    })
}